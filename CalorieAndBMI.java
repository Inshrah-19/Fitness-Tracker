public interface CalorieCalculator {
    double calculateBMR(double weight, int height, int age);
    double calculateTDEE(double BMR);
}
public class MaleCalorieCalculator implements CalorieCalculator {
    @Override
    public double calculateBMR(double weight, int height, int age) {
        return 88.362 + (13.397 * weight) + (4.799 * height) - (5.677 * age);
    }

    @Override
    public double calculateTDEE(double BMR) {
        return BMR * 1.55;
    }
}
public class FemaleCalorieCalculator implements CalorieCalculator {
    @Override
    public double calculateBMR(double weight, int height, int age) {
        return 447.593 + (9.247 * weight) + (3.098 * height) - (4.330 * age);
    }

    @Override
    public double calculateTDEE(double BMR) {
        return BMR * 1.55;
    }
}

