private void saveToDatabase(int height, String gender, double weight, int age, double BMR, double TDEE) {
        String url = "jdbc:ucanaccess://C:/Users/HP/Desktop/Fitness Tracker.accdb";

        String sql = "INSERT INTO CALORIE (height, gender, weight, age, BMR, TDEE) VALUES (?, ?, ?, ?, ?, ?)";

        try (Connection conn = DriverManager.getConnection(url);
             PreparedStatement pstmt = conn.prepareStatement(sql)) {

            pstmt.setInt(1, height);
            pstmt.setString(2, gender);
            pstmt.setDouble(3, weight);
            pstmt.setInt(4, age);
            pstmt.setDouble(5, BMR);
            pstmt.setDouble(6, TDEE);

            pstmt.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data saved successfully!");

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error saving data: " + e.getMessage(), "Database Error", JOptionPane.ERROR_MESSAGE);
        }}
