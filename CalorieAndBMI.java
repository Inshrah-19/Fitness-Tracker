    private void CALCULATEBUTTONActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CALCULATEBUTTONActionPerformed
        String ageText = AGEFIELD.getText();
        String heightText = HEIGHTFIELD.getText();
        String weightText = WEIGHTFEILD.getText();
        boolean isMale = MRADIO.isSelected();

        if (ageText.isEmpty() || heightText.isEmpty() || weightText.isEmpty() || (!isMale && !FRADIO.isSelected())) {
            JOptionPane.showMessageDialog(null, "Please fill all fields and select gender.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            int age = Integer.parseInt(ageText);
            int height = Integer.parseInt(heightText);
            double weight = Double.parseDouble(weightText);

            CalorieCalculator calculator;

            if (isMale) {
                calculator = new MaleCalorieCalculator();
            } else {
                calculator = new FemaleCalorieCalculator();
            }

            double BMR = calculator.calculateBMR(weight, height, age);
            double TDEE = calculator.calculateTDEE(BMR);
            double weightLossCalories = TDEE - 500;
            double weightGainCalories = TDEE + 500;

            ANSWETEXTAREA.setText("Your daily calorie needs:" +
                "\nBMR: " + BMR + " calories/day" +
                "\nTDEE: " + TDEE + " calories/day" +
                "\nTo lose weight: " + weightLossCalories + " calories/day" +
                "\nTo gain weight: " + weightGainCalories + " calories/day" +
                "\n\nBMR: The energy your body uses at rest." +
                "\nTDEE: The total calories you burn in a day, including activity and digestion.");
            saveToDatabase(height, isMale ? "Male" : "Female", weight, age, BMR, TDEE);

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Please enter valid numbers for age, height, and weight.", "Input Error",
                JOptionPane.ERROR_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "An unexpected error occurred: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_CALCULATEBUTTONActionPerformed

